fun variables() {
    //    println("Hello World")

    // can declare variables in this fashion
    var name: String = "Kennedy"
    println(name)

    // can also do this
    var name2: String
    name2 = "Waithaka"
    println(name2)

    // integers
    var age: Int
    age = 24
    println(age)

    // Doubles
    var ratio: Double
    ratio = age / 7.0
    println(ratio)


    // string concatenation
    println("My Name is $name $name2")

    // var and val
    // var - variable
    // val - value, constant can used without type specification
    val phoneNumber = "071245678"
    println(phoneNumber)
}